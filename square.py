
def area(a):
    '''
    Returns the area of a square with a given side

        Parameters:
            a (double): floating point side
        
        Return values:
            square_area (double): floating point area of a square
    '''
    return a * a


def perimeter(a):
    '''
    Returns the perimeter of a square with a given side

        Parameters:
            a (double): floating point side
        
        Return values:
            square_perimeter (double): floating point perimeter of a square
    '''
    return 4 * a
